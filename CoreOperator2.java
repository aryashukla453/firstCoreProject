package firstCoreProject;
//Relational operators
public class CoreOperator2 {
public static void main(String[] args) {
	int x=12,y=14;
	System.out.println("<:" + (x<y));
	System.out.println(">:" + (x>y));
	System.out.println("==:" + (x==y));
	System.out.println("!=:" + (x!=y));
	System.out.println("<=:" + (x<=y));
	System.out.println(">=:" + (x>=y));
}
}
